def load_json(path: Path):
    return json.loads(path.read_text(encoding="utf-8"))


def stage_for(chapter_no: int) -> str:
    if chapter_no <= 4:
        return "起步试错期"
    if chapter_no <= 10:
        return "方法成形期"
    if chapter_no <= 16:
        return "扩张与回撤期"
    if chapter_no <= 21:
        return "专业化总结期"
    return "收官警示期"


def find_excerpt_page(entry: dict, excerpt: str, page_map: list[dict]) -> int:
    for page in page_map:
        if page["entry_index"] == entry["index"] and excerpt in page["text"]:
            return int(page["page"])
    return int(entry["start_page"])


def build_output() -> dict:
    workspace = Path(__file__).resolve().parents[2]
    structure = load_json(workspace / "data" / "metadata" / "book_structure.json")
    page_map = load_json(workspace / "data" / "metadata" / "page_map.json")

    chapters_out = []
    appendix_out = None

    for entry in structure["entries"]:
        if entry["kind"] == "chapter":
            match = re.match(r"^(\d+)\s", entry["title"])
            if not match:
                continue
            chapter_no = int(match.group(1))
            seed = CHAPTERS[chapter_no]
            chart_image = entry["extracted_images"][0] if entry["extracted_images"] else None
            excerpt_page = find_excerpt_page(entry, seed["excerpt"], page_map)
            chapters_out.append(
                {
                    "chapter_no": chapter_no,
                    "stage": stage_for(chapter_no),
                    "full_title": entry["title"],
                    "title": entry["short_title"],
                    "start_page": entry["start_page"],
                    "end_page": entry["end_page"],
                    "page_count": entry["page_count"],
                    "quote_page": excerpt_page,
                    "chart_image": chart_image,
                    **seed,
                }
            )
        elif entry["kind"] == "appendix" and appendix_out is None:
            excerpt_page = find_excerpt_page(entry, APPENDIX["excerpt"], page_map)
            appendix_out = {
                "full_title": "附录合辑：年表、交易规则与最小阻力路线",
                "title": "附录合辑",
                "start_page": 325,
                "end_page": 340,
                "page_count": 16,
                "quote_page": excerpt_page,
                "chart_images": [
                    p["extracted_images"][0]
                    for p in structure["entries"]
                    if p["kind"] == "appendix" and p["extracted_images"]
                ],
                **APPENDIX,
            }

    output = {
        "meta": BOOK_META,
        "chapters": chapters_out,
        "appendix": appendix_out,
    }
    return output


def main() -> None:
    workspace = Path(__file__).resolve().parents[2]
    out = build_output()
    out_path = workspace / "data" / "metadata" / "book_content.json"
    out_path.write_text(json.dumps(out, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Wrote {out_path}")


if __name__ == "__main__":
    main()
